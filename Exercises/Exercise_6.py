'''Exercise 6: Shape

Predict the shape:

a = np.array([1,2,3,4])

b = np.array([[1,2,3],
              [4,5,6]])

c = np.array([
    [[1,2],[3,4]],
    [[5,6],[7,8]]
])'''

import numpy as np

a = np.array([1,2,3,4])

b = np.array([[1,2,3],
              [4,5,6]])

c = np.array([
    [[1,2],[3,4]],
    [[5,6],[7,8]]])

print(a.shape)
print(b.shape)
print(c.shape)